def test_backend_unaccepted_advisory_blocks(tmp_path):
    policy = _load_policy()
    lock, requirements = _backend_files(tmp_path)

    result = policy.evaluate_backend(
        audit=_backend_audit(),
        lock_path=lock,
        requirements_path=requirements,
        exceptions=_ledger(),
        now=NOW,
    )

    assert result["status"] == "FAIL"
    assert result["blockers"] == [
        {
            "ecosystem": "backend-python",
            "package": "demo",
            "advisory": "GHSA-abcd-1234-wxyz",
            "advisory_aliases": ["CVE-2026-1000", "GHSA-alias-1234-wxyz"],
            "installed_version": "1.2.3",
            "dependency_scope": "runtime",
            "severity": "unavailable",
            "reachability": "production-reachable",
            "fix_versions": ["1.2.4"],
        }
    ]
    assert "description" not in json.dumps(result)


def test_one_exact_unexpired_exception_matches_only_bound_advisory_version_and_lock(tmp_path):
    policy = _load_policy()
    lock, requirements = _backend_files(tmp_path)
    exact = _exception(lock)

    accepted = policy.evaluate_backend(
        audit=_backend_audit(),
        lock_path=lock,
        requirements_path=requirements,
        exceptions=_ledger(exact),
        now=NOW,
    )

    assert accepted["status"] == "PASS"
    assert accepted["blockers"] == []
    assert accepted["accepted_exceptions"] == [
        {
            "ecosystem": "backend-python",
            "package": "demo",
            "advisory": "GHSA-abcd-1234-wxyz",
            "advisory_aliases": ["CVE-2026-1000", "GHSA-alias-1234-wxyz"],
            "installed_version": "1.2.3",
            "lock_sha256": sha256(lock.read_bytes()).hexdigest(),
            "reachability": "production-reachable",
            "expires_at": "2026-08-19T09:00:00Z",
        }
    ]

    mutations = {
        "advisory": "GHSA-other-1234-wxyz",
        "advisory_aliases": ["CVE-2026-9999", "GHSA-alias-1234-wxyz"],
        "installed_version": "1.2.2",
        "lock_sha256": "f" * 64,
        "dependency_scope": "development",
        "expires_at": "2026-07-19T08:59:59Z",
    }
    for field, replacement in mutations.items():
        changed = deepcopy(exact)
        changed[field] = replacement
        with pytest.raises(policy.DependencyPolicyError):
            policy.evaluate_backend(
                audit=_backend_audit(),
                lock_path=lock,
                requirements_path=requirements,
                exceptions=_ledger(changed),
                now=NOW,
            )


