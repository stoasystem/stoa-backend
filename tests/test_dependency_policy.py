def test_duplicate_and_stale_unmatched_exceptions_fail_closed(tmp_path):
    policy = _load_policy()
    lock, requirements = _backend_files(tmp_path)
    exact = _exception(lock)

    with pytest.raises(policy.DependencyPolicyError, match="duplicate"):
        policy.evaluate_backend(
            audit=_backend_audit(),
            lock_path=lock,
            requirements_path=requirements,
            exceptions=_ledger(exact, deepcopy(exact)),
            now=NOW,
        )

    stale = _exception(lock, advisory="GHSA-stale-1111-2222")
    with pytest.raises(policy.DependencyPolicyError, match="unmatched"):
        policy.evaluate_backend(
            audit={"dependencies": [{"name": "demo", "version": "1.2.3", "vulns": []}], "fixes": []},
            lock_path=lock,
            requirements_path=requirements,
            exceptions=_ledger(stale),
            now=NOW,
        )


